/*
 Relational Operator :- will give the boolean value based onn comparision
 > greater than
 < less than
 >= greater than or equal to
 <= less than or equal to
 

 */

let a = 10;
let b = 20;

console.log("a > b :", a > b); // false

console.log("a < b :", a < b); // true

console.log("a >= b :", a >= b); // false

console.log("a <= b :", a <= b); // true
